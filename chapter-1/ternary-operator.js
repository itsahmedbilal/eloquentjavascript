/** Ternary Operator
 * Ternary Expression (condition ? expr1 : expr2): A shorthand for if-else statements.
 * Example: let status = age >= 18 ? "adult" : "minor";
 */

// Example of the ternary operator
let age = 20; // Initialize variable age with value 20
let status = age >= 18 ? "adult" : "minor"; // If age is greater than or equal to 18, status is "adult", otherwise "minor"

console.log(status); // Outputs: "adult"
